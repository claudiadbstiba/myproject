        <h3 class="p__item"> 59 Important Self-Analysis Test Questions <br> -- Napoleon Hill</h3>
        <p class="p__item" >
            To be EMOTIONALLY STRONG, you first need to KNOW YOURSELF WELL. <br> The following 59 questions will help you understand yourself better <br> (Taken out from Napoleon Hill’s THINK AND GROW RICH) <br><br>
        
        59 Important Self-Analysis Test Questions <br><br>
        
        1. Do you complain often of feeling bad, and if so, what is the cause? <br>
        2.Do you find fault with other people at the slightest provocation?<br>
        3. Do you frequently make mistakes in your work, and if so, why? Are you sarcastic and offensive in your conversation? <br>
        4. Do you deliberately avoid the association of anyone, and if so, why? <br>
        5. Do you suffer frequently with indigestion? If so, what is the cause? <br>
        6. Does life seem futile and the future hopeless to you? If so, why? <br>
        7. Do you like your occupation? If not, why? <br>
        8. Do you often feel self-pity, and if so, why? <br>
        9. Are you envious of those who excel you? <br>
        10. To which do you devote most time, thinking of SUCCESS or of FAILURE? <br>
        11. Are you gaining or losing self-confidence as you grow older? <br>
        12. Do you learn something of value from all mistakes? <br>
        13. Are you permitting some relative or acquaintance to worry you? If so, why? <br>
        14. Are you sometimes “in the clouds” and at other times “in the depths” of despondency? <br>
        15. Who has the most inspiring influence upon you? What is the cause? <br>
        16. Do you tolerate negative or discouraging influences which you can avoid? <br>
        17. Are you careless of your personal appearance? If so, when and why? <br>
        18. Have you learned how to drown your troubles by being too busy to be annoyed by them? <br>
        19. Would you call yourself a “spineless weakling” if you permitted others to do your thinking for you? <br>
        20. Do you neglect internal bathing until autointoxication makes you ill-tempered and irritable? <br>
        21. How many preventable disturbances annoy you, and why do you tolerate them? <br>
        22. Do you resort to liquor, pills, narcotics, or cigarettes to quiet your nerves? If so, why do you not try willpower instead? <br>
        23. Does anyone nag you, and if so, for what reason? <br>
        24. Do you have a DEFINITE CHIEF AIM IN LIFE, and if so, what is it and what plan have you for achieving it? <br>
        25. Do you suffer from any of the Six Basic Fears? If so, which ones? <br>
        26. Have you a method by which you can shield yourself against the negative influence of others? <br>
        27. Do you make deliberate use of autosuggestion to make your mind positive? <br>
        28. Which do you value most, your material possessions or your privilege of controlling your own thoughts? <br>
        29. Are you easily influenced by others, against your own judgment? <br>
        30. Has today added anything of value to your stock of knowledge or state of mind? <br>
        31. Do you face squarely the circumstances which make you unhappy, or do you sidestep the responsibility? <br>
        32. Do you analyze all mistakes and failures and try to profit by them, or do you take the attitude that this is not your duty? <br>
        33. Can you name three of your most damaging weaknesses? What are you doing to correct them? <br>
        34. Do you encourage other people to bring their worries to you for sympathy? <br>
        35. Do you choose, from your daily experiences, lessons or influences which aid in your personal advancement? <br>
        36. Does your presence have a negative influence on other people as a rule? <br>
        37. What habits of other people annoy you most? <br>
        38. Do you form your own opinions, or permit yourself to be influenced by other people? <br>
        39. Have you learned how to create a mental state of mind with which you can shield yourself against all discouraging influences? <br>
        40. Does your occupation inspire you with faith and hope? <br>
        41. Are you conscious of possessing spiritual forces of sufficient power to enable you to keep your mind free from all forms of FEAR?<br> 
        42. Does your religion help you to keep your own mind positive? <br>
        43. Do you feel it your duty to share other people’s worries? If so, why? <br>
        44. If you believe that birds of a feather flock together, what have you learned about yourself by studying the friends whom you attract? <br>
        45. What connection, if any, do you see between the people with whom you associate most closely and any unhappiness you may experience? <br> 
        46. Could it be possible that some person whom you consider to be a friend is, in reality, your worst enemy because of his or her negative influence on your mind? <br>
        47. By what rules do you judge who is helpful and who is damaging to you? <br>
        48. Are your intimate associates mentally superior or inferior to you? <br>
        49. How much time out of every 24 hours do you devote to: a. your occupation/ b. sleep/ c. play and relaxation/ d. acquiring useful knowledge/ e. plain waste <br>
        50. Who among your acquaintances: a. encourages you most/ b. cautions you most/ c. discourages you most/ d. helps you most in other ways <br>
        51. What is your greatest worry? Why do you tolerate it? <br>
        52. When others offer you free, unsolicited advice, do you accept it without question or analyze their motive? <br>
        53. What, above all else, do you most DESIRE? Do you intend to acquire it? Are you willing to subordinate all other desires for this one? How much time daily do you devote to acquiring it? <br>
        54. Do you change your mind often? If so, why? <br>
        55. Do you usually finish everything you begin? <br>
        56. Are you easily impressed by other people’s business or professional titles, college degrees, or wealth? Are you easily influenced by what other people think or say of you? <br>
        57. Do you cater to people because of their social or financial status? <br>
        58. Whom do you believe to be the greatest person living? In what respect is this person superior to yourself? <br>
        59. How much time have you devoted to studying and answering these questions? <br>
        (At least one day is necessary for the thoughtful analysis and the full answering of the entire list.) <br>
        
        If you have answered all these questions truthfully, you know more about yourself than the majority of people. Study the questions carefully, come back to them once each week for several months, and be astounded at the amount of additional knowledge of great value to yourself you will have gained by the simple method of answering the questions truthfully. If you are not certain concerning the answers to some of the questions, seek the counsel of those who know you well, especially those who have no motive in flattering you, and see yourself through their eyes. The experience will be astonishing.
        
